  <p>
    Spiders (order Araneae) are air-breathing arthropods that have eight legs and chelicerae with fangs able to inject venom.
  </p>
  <p>
    They are the largest order of arachnids and rank seventh in total species diversity among all orders of organisms.
  </p>
  <p>
    Spiders are found worldwide on every continent except for Antarctica, and have become established in nearly every habitat with the exceptions of air and sea colonization.
  </p>
  <p>
    As of November 2015, at least 45,700 spider species, and 113 families have been recorded by taxonomists.
  </p>
  <p>
    However, there has been dissension within the scientific community as to how all these families should be classified, as evidenced by the over 20 different classifications that have been proposed since 1900.
  </p>

